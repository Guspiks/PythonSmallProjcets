# pierwszym soposowbem na uzywanie zmiennych liczbowych jest użycie funkcji int() lecz jest ona tylko do liczb całkowitych
num1=input("Podaj pierwszą liczbę: ")
num2=input("Podaj drugą liczbę: ")
wynik=int(num1)+int(num2)
print(wynik)
# drugi sposób to użycie zmiennych float które oddaja zmiennoprzecinkowe
wynik=float(num1)+float(num2)
print(wynik)
